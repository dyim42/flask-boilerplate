#!/usr/bin/env python
#coding=utf-8

from flask_boilerplate.extensions import db

class User(db.Model):
    __tablename__ = 'user'
    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(80))

    def __init__(self, name):
        self.name = name

    def __repr__(self):
        return '<User %r>' % self.name

